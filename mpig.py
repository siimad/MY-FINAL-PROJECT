# Create additional features that might help predict floods

# Lag features (previous months' precipitation)
df = df.sort_values(['Year', 'Month'])
df['Prev_Month_Precip'] = df.groupby('Year')['Precipitation'].shift(1)

# Rolling averages (3-month and 6-month)
df['Precip_3mo_avg'] = df.groupby('Year')['Precipitation'].transform(lambda x: x.rolling(3, min_periods=1).mean())
df['Precip_6mo_avg'] = df.groupby('Year')['Precipitation'].transform(lambda x: x.rolling(6, min_periods=1).mean())

# Temperature range
df['Temp_Range'] = df['Tmax'] - df['Tmin']

# Seasonal indicators (one-hot encoding)
season_dummies = pd.get_dummies(df['Season'], prefix='Season')
df = pd.concat([df, season_dummies], axis=1)

# Month as cyclical feature (to capture seasonal patterns)
df['Month_Sin'] = np.sin(2 * np.pi * df['Month']/12)
df['Month_Cos'] = np.cos(2 * np.pi * df['Month']/12)

# Fill NaN values from lag features
df = df.fillna(method='bfill').fillna(method='ffill')

