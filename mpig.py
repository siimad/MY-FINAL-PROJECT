def predict_flood_risk(year, month, tmin, tmax, tmean, precip, season, model, scaler, feature_cols):
    """
    Function to predict flood risk for new data
    """
    # Create a DataFrame with the input data
    new_data = pd.DataFrame({
        'Year': [year],
        'Month': [month],
        'Tmin': [tmin],
        'Tmax': [tmax],
        'Tmean': [tmean],
        'Precipitation': [precip]
    })

    # Add season dummy variables
    for s in ['Autumn', 'Spring', 'Summer', 'Winter']:
        new_data[f'Season_{s}'] = 1 if season == s else 0

    # Add engineered features
    new_data['Prev_Month_Precip'] = precip  # Simplified - in practice, you'd need historical data
    new_data['Precip_3mo_avg'] = precip
    new_data['Precip_6mo_avg'] = precip
    new_data['Temp_Range'] = tmax - tmin
    new_data['Month_Sin'] = np.sin(2 * np.pi * month/12)
    new_data['Month_Cos'] = np.cos(2 * np.pi * month/12)

    # Ensure columns are in the same order as training data
    new_data = new_data[feature_cols]

    # Scale the data
    new_data_scaled = scaler.transform(new_data)

    # Make prediction
    prediction = model.predict(new_data_scaled)[0]
    probability = model.predict_proba(new_data_scaled)[0]

    risk_levels = ['No Flood', 'Flood']

    return {
        'prediction': risk_levels[prediction],
        'probability_no_flood': probability[0],
        'probability_flood': probability[1]
    }

